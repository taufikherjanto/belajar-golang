package main

import "fmt"

func main() {
	// Int
	umur := 34

	// String
	nama := "Taufik"

	// Float
	suhu := 35.6

	// Byte
	var binaryData byte = 0b11010101

	// Boolean
	isMentor := false

	// nil
	var noValue interface{}

	fmt.Println("Umur: ", umur)
	fmt.Println("Nama: ", nama)
	fmt.Println("Suhu: ", suhu)
	fmt.Println("Binary data: ", binaryData)
	fmt.Println("Is mentor: ", isMentor)
	fmt.Println("No value: ", noValue)

}
